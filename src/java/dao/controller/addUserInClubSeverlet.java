    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        processRequest(request, response);
    } 

    /** 
     * Handles the HTTP <code>POST</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        String userID = request.getParameter("userID");
        String status = request.getParameter("status");
        
        if(userID != null || status != null){
            UsersDAO.updateStatusSignUp(status, Integer.parseInt(userID));
            if(status.equals("ACCEPTED")){
                String error = "Đã Chấp Nhận Vào Câu Lạc Bộ";
                request.setAttribute("checkevent1", error);
                RequestDispatcher rs = request.getRequestDispatcher("view/Vicechairman.jsp");
                rs.forward(request, response);
            }else {
                String error = "Đã Từ Chối Vào Câu Lạc Bộ";
                request.setAttribute("checkevent1", error);
                RequestDispatcher rs = request.getRequestDispatcher("view/Vicechairman.jsp");
                rs.forward(request, response);
            }
        }
    }

